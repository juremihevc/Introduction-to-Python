number_a = int(input("Enter first number: "))
operator = input("Enter operator (+ , - , * , / ): ")
number_b = int(input("Enter second number: "))

if operator == "+":
    print(number_a + number_b)

if operator == "-":
    print(number_a - number_b)

if operator == "*":
    print(number_a * number_b)

if operator == "/":
    print(number_a / number_b)
